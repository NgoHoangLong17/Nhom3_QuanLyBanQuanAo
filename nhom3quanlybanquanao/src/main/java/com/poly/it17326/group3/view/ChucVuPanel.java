    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int index = tbChucVu.getSelectedRow();
        ChucVu chucVu = viewChucVuService.getChucVu().get(index);
        chucVu.setTenChucVu(txtTenCV.getText());
        if(viewChucVuService.sua(chucVu)){
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            loadTable((ArrayList<ChucVu>) viewChucVuService.getChucVu());
        }else{
            JOptionPane.showMessageDialog(this, "Sửa thất bại");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        ChucVu chucVu = new ChucVu();
        chucVu.setTenChucVu(txtTenCV.getText());
        if(viewChucVuService.them(chucVu)){
            JOptionPane.showMessageDialog(this, "Thêm thành công");
            loadTable((ArrayList<ChucVu>) viewChucVuService.getChucVu());
        }else{
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void tbChucVuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbChucVuMouseClicked
        // TODO add your handling code here:
        int row = tbChucVu.getSelectedRow();
        txtId.setText(tbChucVu.getValueAt(row, 0).toString());
        txtTenCV.setText(tbChucVu.getValueAt(row, 1).toString());
    }//GEN-LAST:event_tbChucVuMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int index = tbChucVu.getSelectedRow();
        ChucVu chucVu = viewChucVuService.getChucVu().get(index);
        if(index == -1){
            JOptionPane.showMessageDialog(this, "Xóa thất bại");
        }else if(viewChucVuService.xoa(chucVu)){
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            loadTable((ArrayList<ChucVu>) viewChucVuService.getChucVu());
        }else{
            JOptionPane.showMessageDialog(this, "Xóa thất bại");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

