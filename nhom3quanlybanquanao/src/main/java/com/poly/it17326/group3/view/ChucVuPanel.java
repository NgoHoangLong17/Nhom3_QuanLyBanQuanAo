    public void loadTable(ArrayList<ChucVu> ListChucVu){
        defaultTableModel = (DefaultTableModel) tbChucVu.getModel();
        defaultTableModel.setColumnIdentifiers(new String[]{"ID", "Tên chức vụ"});
        defaultTableModel.setRowCount(0);
        for(ChucVu chucVu : ListChucVu){
            defaultTableModel.addRow(new Object[]{chucVu.getId(), chucVu.getTenChucVu()});
        }
    }

